void main() {
  String input = "Monday"; // Change this string to test different cases
  
  switch (input) {
    case "Monday":
      print("It's the start of the week.");
      break;
    case "Tuesday":
      print("You're getting into the groove.");
      break;
    case "Wednesday":
      print("Halfway through the week!");
      break;
    case "Thursday":
      print("Almost there, just one more day.");
      break;
    case "Friday":
      print("TGIF! The weekend is almost here.");
      break;
    case "Saturday":
       print("Atlast vibe to your best");
       break;
    case "Sunday":
      print("Weekend vibes!");
      break;
    default:
      print("Invalid day.");
  }
}
